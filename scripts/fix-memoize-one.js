// scripts/fix-memoize-one.js
const fs = require("fs");
const path = require("path");

function ensureDir(p) {
  if (!fs.existsSync(p)) fs.mkdirSync(p, { recursive: true });
}

const pkgDir = path.join(process.cwd(), "node_modules", "memoize-one");
const targetDir = path.join(pkgDir, "dist");
const targetFile = path.join(targetDir, "memoize-one.cjs.js");

// Si no existe el paquete, no hacemos nada (pero en tu log sí existe).
if (!fs.existsSync(pkgDir)) {
  console.log("[fix-memoize-one] memoize-one not found, skipping.");
  process.exit(0);
}

// Si el archivo ya existe, listo.
if (fs.existsSync(targetFile)) {
  console.log("[fix-memoize-one] OK, dist file already exists.");
  process.exit(0);
}

// Creamos dist/ y escribimos una implementación mínima compatible.
ensureDir(targetDir);

const code = `
// Auto-generated by scripts/fix-memoize-one.js
'use strict';

function defaultIsEqual(newArgs, lastArgs) {
  if (newArgs.length !== lastArgs.length) return false;
  for (var i = 0; i < newArgs.length; i++) {
    if (newArgs[i] !== lastArgs[i]) return false;
  }
  return true;
}

function memoizeOne(resultFn, isEqual) {
  if (typeof resultFn !== 'function') {
    throw new Error('memoize-one: expected a function');
  }

  var equalityFn = typeof isEqual === 'function' ? isEqual : defaultIsEqual;

  var lastThis;
  var lastArgs = null;
  var lastResult;
  var called = false;

  function memoized() {
    var newThis = this;
    var newArgs = Array.prototype.slice.call(arguments);

    if (called && lastArgs && lastThis === newThis && equalityFn(newArgs, lastArgs)) {
      return lastResult;
    }

    lastThis = newThis;
    lastArgs = newArgs;
    lastResult = resultFn.apply(newThis, newArgs);
    called = true;
    return lastResult;
  }

  memoized.clear = function () {
    called = false;
    lastThis = undefined;
    lastArgs = null;
    lastResult = undefined;
  };

  return memoized;
}

// CommonJS + compat con default/named import
module.exports = memoizeOne;
module.exports.default = memoizeOne;
module.exports.memoizeOne = memoizeOne;
`;

fs.writeFileSync(targetFile, code.trimStart(), "utf8");
console.log("[fix-memoize-one] Wrote:", targetFile);
